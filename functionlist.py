def propagate(w,b,X,Y):
    '''
    vars:
    m = number of training examples
    n = number of features for every training example
    A (1,m) = array of sigmoid of (w.T X + b) for each training example
    dw (n,1)
    db ()
    Argument:
    w (n,1)
    b ()
    X (m,n)
    Y (m,) => convert rank 1 array to rank 2 array Y_reshape(m,1)

    Return:
    grads {"dw": dw, "db": db}
    cost ()
    '''
    m, n = X.shape

    A = sigmoid(np.dot(w.T,X.T)+b)

    Y_reshape = np.reshape(Y, (-1,1))

    cost = - 1/m * np.sum(np.dot(Y_reshape.T,np.log(A.T))+np.dot((1-Y_reshape.T),np.log(1-A.T)))

    dw = 1/m * (np.dot(X.T,(A.T-Y_reshape)))
    db = 1/m * np.sum(A.T-Y_reshape)

    grads = {"dw": dw,
                "db": db}

    return grads, cost


def predict(w,b,X):
	'''
	vars:
	m = number of training examples
	n = number of features for every training example
	A (1,m) = array of sigmoid of (w.T X + b) for each training example
	funcs:
    sigmoid() sigmoid function
	args:
	w (n,1)
	b ()
	X (m,n)

	return:
	Y_prediction (m,1) 

	'''
	m,n = X.shape
	Y_prediction = np.zeros((m,1))

	A = sigmoid(np.dot(w.T,X.T)+b)

	for i in range(A.shape[1]):
		if A[0,i] > 0.5:
			Y_prediction[i,0] = 1
		else:
			Y_prediction[i,0] = 0
	
	return Y_prediction


def optimize(w,b,X,Y,num_iterations=100,learning_rate=0.009,print_cost=False):
    '''
    var:
    m = number of training examples
	n = number of features for every training example
    dw (n,1)
    db ()
    args:
    w (n,1)
    b ()
    return:
    params {"w": w, "b": b}
    grads {"dw": dw, "db": db}
    costs []
    '''
    w = copy.deepcopy(w)
    b = copy.deepcopy(b)

    costs = []

    for i in range(num_iterations):
        grads, cost = propagate(w,b,X,Y)

        dw = grads["dw"]
        db = grads["db"]

        w = w - learning_rate*dw
        b = b - learning_rate*db



        if i % 100 == 0:
            costs.append(cost)
        
            # Print the cost every 100 training iterations
            if print_cost:
                print ("Cost after iteration %i: %f" %(i, cost))

    params = {"w": w,
                "b": b}
                
    grads = {"dw": dw,
                "db": db}


    return params, grads, costs

